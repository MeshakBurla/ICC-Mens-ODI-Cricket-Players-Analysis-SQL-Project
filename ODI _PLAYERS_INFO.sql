--Q2.	Calculate the runs each player:

SELECT PLAYER_NAME,TOTAL_RUNS FROM ODI_PLAYERS_INFO2 ORDER BY TOTAL_RUNS;

--Q3.	Find the each player  average of runs in ODIs:

SELECT PLAYER_NAME,AVG(TOTAL_RUNS) AS  AVERAGE_OF_RUNS_IN_ODI FROM ODI_PLAYERS_INFO2
GROUP BY PLAYER_NAME;

--Q4.	Identify the player name with highest runs in ODIs

SELECT  MAX(TOTAL_RUNS)AS HIGHT_RUNS_IN_ODI FROM ODI_PLAYERS_INFO2;

SELECT PLAYER_NAME,MAX(TOTAL_RUNS) FROM ODI_PLAYERS_INFO2
