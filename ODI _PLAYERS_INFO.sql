SELECT PLAYER_NAME,ECONOMY_RATE FROM ODI_PLAYERS_INFO2 
WHERE ECONOMY_RATE < 5.00;

--Q8.	Identify  players above 5 wickets taker in single match in ODIs:

SELECT PLAYER_NAME,BEST_WICKETS FROM ODI_PLAYERS_INFO2 WHERE BEST_WICKETS > 5;

--Q9.	Find the team name and player name with highest centuries in ODIs:

SELECT TEAM_NAME,PLAYER_NAME,MAX(CENTURIES) AS HIGHEST_CENTURIES FROM ODI_PLAYERS_INFO2
GROUP BY TEAM_NAME,PLAYER_NAME;



